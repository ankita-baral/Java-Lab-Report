public class LogicalOperators {
    public static void main(String[] args) {
        boolean a = true, b = false;
        System.out.println("a AND b: " + (a && b));  // Logical AND
        System.out.println("a OR b: " + (a || b));   // Logical OR
        System.out.println("NOT a: " + (!a));        // Logical NOT
    }
}
